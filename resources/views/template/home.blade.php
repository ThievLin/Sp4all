                    <div class="title-accordion" style="color: orange;"> What Is A SP4ALL ? </div>
                    <p>The SP4ALL is a CSO led coalition aspiring to raise citizen’s awareness on social protection and elevate citizen’s concerns to government organisations. The coalition will be oversighted and supported by various civil society organisations (CSOs) in Cambodia seeking to learn and share knowledge around social protection topics.</p> <br>
                    <p>The coalition aims to represent all citizens to social protection, voice their demands in policy development, help inform policy and promote synergies and collective action for a more comprehensive, inclusive, accountable and gender transformative social protection system that benefits all citizens. </p> <br>
                    <p>SP4ALL intends to represent the “demand side” and citizens; members of CSOs; international organisations; workers, women, and youth groups; Trade Unions; workers associations; and especially person with disabilities, etc., and Indigenous community members in an active movement for policy change that is recognized, visible and influential nationally and regionally. </p> <br>
                    <p>SP4ALL aims to give a voice to citizens so their needs and concerns can be amplified by CSOs and their representatives and brought to the attention of practitioners and policy makers. It will be a space where members can meet, discuss ideas, analyze policies and legislations, plan strategies for influencing and build joint agendas for change.</p>
